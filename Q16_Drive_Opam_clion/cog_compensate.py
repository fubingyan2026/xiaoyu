def simple_comp_table(data, table_size=128):
    """
    最简单的补偿表生成函数（纯Python，无依赖）
    """
    # 1. 预处理数据
    # 按角度分组，收集每个角度对应的所有电流值
    angle_dict = {}
    
    for angle, current in data:
        angle_norm = angle % 360
        if angle_norm not in angle_dict:
            angle_dict[angle_norm] = []
        angle_dict[angle_norm].append(current)
    
    # 2. 计算每个角度的平均值
    avg_dict = {}
    for angle in range(360):
        if angle in angle_dict and angle_dict[angle]:
            avg_dict[angle] = sum(angle_dict[angle]) / len(angle_dict[angle])
    
    # 3. 填充缺失的角度值
    # 查找每个缺失角度的最近有效角度
    filled_avg = [0] * 360
    
    for angle in range(360):
        if angle in avg_dict:
            filled_avg[angle] = avg_dict[angle]
        else:
            # 寻找最近的有效角度
            found = False
            for offset in range(1, 180):  # 搜索半径180度
                prev_angle = (angle - offset) % 360
                next_angle = (angle + offset) % 360
                
                if prev_angle in avg_dict and next_angle in avg_dict:
                    # 两个方向都有值，取平均
                    filled_avg[angle] = (avg_dict[prev_angle] + avg_dict[next_angle]) / 2
                    found = True
                    break
                elif prev_angle in avg_dict:
                    filled_avg[angle] = avg_dict[prev_angle]
                    found = True
                    break
                elif next_angle in avg_dict:
                    filled_avg[angle] = avg_dict[next_angle]
                    found = True
                    break
            
            if not found:
                filled_avg[angle] = 0  # 默认值
    
    # 4. 简单的平滑滤波（3点平均）
    smoothed = filled_avg.copy()
    for i in range(360):
        prev = (i - 1) % 360
        next_idx = (i + 1) % 360
        smoothed[i] = (filled_avg[prev] + filled_avg[i] + filled_avg[next_idx]) / 3
    
    # 5. 生成补偿表
    comp_table = []
    for i in range(table_size):
        angle = i * 360.0 / table_size
        
        idx1 = int(angle)
        idx2 = (idx1 + 1) % 360
        
        alpha = angle - idx1
        
        comp = smoothed[idx1] * (1 - alpha) + smoothed[idx2] * alpha
        comp_table.append(comp)
    
    return comp_table, smoothed

